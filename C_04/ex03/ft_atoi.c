int	ft_atoi(char *str)
{
	int	i;
	int	s;
	int	r;

	i = 0;
	s = 1;
	while (str[i] == 32 || (str[i] > 8 && str[i] < 14))
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		i++;
		if (str[i - 1] == '-')
			s = -s;
	}
	r = 0;
	while (str[i] > 47 && str[i] < 58)
	{
		r = r * 10;
		r = r + str[i] - '0';
		i++;
	}
	r = r * s;
	return (r);
}
