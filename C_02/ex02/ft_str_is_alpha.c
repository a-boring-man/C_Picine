int	ft_str_is_alpha(char *str)
{
	int	i;

	i = -1;
	while (i++, str[i])
	{
		if (!((str[i] > 64 && str[i] < 91) || (str[i] < 123 && str[i] > 96)))
			return (0);
	}
	return (1);
}
