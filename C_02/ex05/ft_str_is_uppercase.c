int	ft_str_is_uppercase(char *str)
{
	int	i;
	int	r;

	i = -1;
	r = 0;
	while (i++, str[i] != '\0')
	{
		if (str[i] < 65 || str[i] > 90)
			r++;
	}
	if (r != 0)
		return (0);
	else
		return (1);
}
